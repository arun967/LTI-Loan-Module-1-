package com.home.model;

public class IncomeDetailsModel {

private String incomeId;
private String employmentType;
private String employerName;
private long monthlyIncome;
private int retirementAge;
private double maxLoanAmountGrantable;


public String getIncomeId() {
	return incomeId;
}
public void setIncomeId(String incomeId) {
	this.incomeId = incomeId;
}
public String getEmploymentType() {
	return employmentType;
}
public void setEmploymentType(String employmentType) {
	this.employmentType = employmentType;
}
public String getEmployerName() {
	return employerName;
}
public void setEmployerName(String employerName) {
	this.employerName = employerName;
}
public long getMonthlyIncome() {
	return monthlyIncome;
}
public void setMonthlyIncome(long monthlyIncome) {
	this.monthlyIncome = monthlyIncome;
}
public int getRetirementAge() {
	return retirementAge;
}
public void setRetirementAge(int retirementAge) {
	this.retirementAge = retirementAge;
}
public double getMaxLoanAmountGrantable() {
	return maxLoanAmountGrantable;
}
public void setMaxLoanAmountGrantable(double maxLoanAmountGrantable) {
	this.maxLoanAmountGrantable = maxLoanAmountGrantable;
}





	
}
