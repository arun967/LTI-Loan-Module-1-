package com.home.model;

public class PropertyDetailsModel {
	
	private long applicationId;
	private String propertyLocation;
	private String propertyName;
	private double estimatedPropertyAmount;
	
	public long getApplicationId() {
		return applicationId;
	}
	public void setApplicationId(long applicationId) {
		this.applicationId = applicationId;
	}
	public String getPropertyLocation() {
		return propertyLocation;
	}
	public void setPropertyLocation(String propertyLocation) {
		this.propertyLocation = propertyLocation;
	}
	public String getPropertyName() {
		return propertyName;
	}
	public void setPropertyName(String propertyName) {
		this.propertyName = propertyName;
	}
	public double getEstimatedPropertyAmount() {
		return estimatedPropertyAmount;
	}
	public void setEstimatedPropertyAmount(double estimatedPropertyAmount) {
		this.estimatedPropertyAmount = estimatedPropertyAmount;
	}
	
	
	
	
	

}
